document.addEventListener("DOMContentLoaded", () => {
    const navMenu = document.getElementById("navMenu");
    const navToggle = document.getElementById("navToggle");
    const portfolioGrid = document.getElementById("portfolioGrid");
    const filterButtons = document.querySelectorAll(".filter-btn");
    const modal = document.getElementById("portfolioModal");
    const modalOverlay = document.getElementById("modalOverlay");
    const modalClose = document.getElementById("modalClose");
    
    // Navbar scroll effect
    window.addEventListener("scroll", () => {
        const navbar = document.querySelector(".navbar");
        if (window.scrollY > 50) {
            navbar.style.boxShadow = "var(--shadow-soft)";
            navbar.style.backgroundColor = "rgba(250, 248, 245, 0.95)";
        } else {
            navbar.style.boxShadow = "none";
            navbar.style.backgroundColor = "rgba(250, 248, 245, 0.85)";
        }
    });

    // Mobile navigation toggle
    if (navToggle && navMenu) {
        navToggle.addEventListener("click", () => {
            navMenu.classList.toggle("open");
            // Toggle hamburger icon between bars and times (close icon)
            const icon = navToggle.querySelector("i");
            if (icon) {
                if (navMenu.classList.contains("open")) {
                    icon.classList.replace("fa-bars", "fa-times");
                } else {
                    icon.classList.replace("fa-times", "fa-bars");
                }
            }
        });
        
        // Close menu when a link is clicked
        const navLinks = document.querySelectorAll(".nav-link");
        navLinks.forEach(link => {
            link.addEventListener("click", () => {
                navMenu.classList.remove("open");
                const icon = navToggle.querySelector("i");
                if (icon) icon.classList.replace("fa-times", "fa-bars");
            });
        });
    }

    // Render portfolio items
    function renderPortfolio(filterCategory = "all") {
        if (!portfolioGrid) return;
        
        portfolioGrid.innerHTML = "";
        
        const filteredItems = filterCategory === "all" 
            ? portfolioItems 
            : portfolioItems.filter(item => item.category === filterCategory);
            
        filteredItems.forEach(item => {
            const card = document.createElement("div");
            card.className = "portfolio-card";
            card.innerHTML = `
                <div class="card-header-accent accent-${item.category}"></div>
                <div class="card-body">
                    <div class="card-meta">
                        <span class="card-tag tag-${item.category}">${item.tag}</span>
                        <span class="card-read-time">${item.readTime}</span>
                    </div>
                    <h3 class="card-title">${item.title}</h3>
                    <p class="card-excerpt">${item.excerpt}</p>
                    <div class="card-footer">
                        <button class="card-link view-project-btn" data-id="${item.id}">
                            Read Sample <span class="arrow-icon">→</span>
                        </button>
                    </div>
                </div>
            `;
            portfolioGrid.appendChild(card);
        });

        // Add event listeners to cards' read buttons
        const viewButtons = document.querySelectorAll(".view-project-btn");
        viewButtons.forEach(btn => {
            btn.addEventListener("click", () => {
                const projectId = btn.getAttribute("data-id");
                openModal(projectId);
            });
        });
    }

    // Modal Control Functions
    function openModal(id) {
        if (!modal) return;
        
        const item = portfolioItems.find(p => p.id === id);
        if (!item) return;

        // Set content
        document.getElementById("modalTag").className = `card-tag tag-${item.category}`;
        document.getElementById("modalTag").innerText = item.tag;
        document.getElementById("modalReadTime").innerText = item.readTime;
        document.getElementById("modalTitle").innerText = item.title;
        document.getElementById("modalIntroBox").className = `modal-intro-box intro-${item.category}`;
        document.getElementById("modalContext").innerText = item.context;
        document.getElementById("modalContent").innerHTML = item.content;

        // Display
        modal.classList.add("active");
        document.body.style.overflow = "hidden"; // Prevent background scroll
    }

    function closeModal() {
        if (!modal) return;
        modal.classList.remove("active");
        document.body.style.overflow = ""; // Restore scroll
    }

    // Modal close listeners
    if (modalClose) modalClose.addEventListener("click", closeModal);
    if (modalOverlay) modalOverlay.addEventListener("click", closeModal);
    
    // Close modal on Escape press
    window.addEventListener("keydown", (e) => {
        if (e.key === "Escape" && modal && modal.classList.contains("active")) {
            closeModal();
        }
    });

    // Tab filtering trigger
    filterButtons.forEach(button => {
        button.addEventListener("click", () => {
            // Remove active class from all buttons
            filterButtons.forEach(btn => btn.classList.remove("active"));
            // Add active class to clicked button
            button.classList.add("active");
            
            const filterValue = button.getAttribute("data-filter");
            renderPortfolio(filterValue);
        });
    });

    // Initial render
    renderPortfolio();

    // Setup actual contact form submission via FormSubmit
    const contactForm = document.getElementById("contactForm");
    if (contactForm) {
        contactForm.addEventListener("submit", (e) => {
            e.preventDefault();
            const btnSubmit = document.getElementById("btnSubmit");
            const originalText = btnSubmit.innerHTML;
            btnSubmit.innerHTML = 'Sending... <i class="fas fa-spinner fa-spin"></i>';
            btnSubmit.disabled = true;

            const formData = new FormData(contactForm);
            
            fetch("https://formsubmit.co/ajax/soumyapdixitofficial@gmail.com", {
                method: "POST",
                body: formData
            })
            .then(response => response.json())
            .then(data => {
                alert("Thank you! Your message has been sent successfully.");
                contactForm.reset();
            })
            .catch(error => {
                alert("Oops! There was a problem submitting your form.");
            })
            .finally(() => {
                btnSubmit.innerHTML = originalText;
                btnSubmit.disabled = false;
            });
        });
    }
});
